# len(s) – độ dài chuỗi

# s.lower() – chuyển thành chữ thường

# s.upper() – chuyển thành chữ hoa

# s.capitalize() – viết hoa chữ cái đầu

# s.title() – viết hoa chữ cái đầu mỗi từ

# s.strip() – loại bỏ khoảng trắng hai đầu

# s.lstrip() / s.rstrip() – loại bỏ khoảng trắng bên trái/phải

# s.split(sep) – tách chuỗi thành list

# sep.join(list) – nối list thành chuỗi

# s.replace(old, new) – thay thế chuỗi

# s.find(sub) – tìm vị trí con (hoặc -1 nếu không có)

# s.index(sub) – giống find nhưng báo lỗi nếu không tìm thấy

# s.count(sub) – đếm số lần xuất hiện

# s.startswith(prefix) / s.endswith(suffix) – kiểm tra bắt đầu/kết thúc

# s.isdigit() – kiểm tra toàn số

# s.isalpha() – kiểm tra toàn chữ cái

# s.isalnum() – kiểm tra chữ + số

# s.isspace() – kiểm tra toàn khoảng trắng
